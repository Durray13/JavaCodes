package SwitchCasePrograms;

public class Prog1 {

	public static void main(String[] args) {
		int op=7;
		switch(op) {
		case 1:
			System.out.println("Switch 1 is pressed");
			break;
		case 2:
			System.out.println("Switch 2 is pressed");
			break;
		default:
			System.out.println("Invalid option");
		}

	}

}
