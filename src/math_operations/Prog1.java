package math_operations;

public class Prog1 {

	public static void main(String[] args) {
		int num1=56;
		int num2=10;
		
		int sum=num1+num2;
		int diff=num1-num2;
		int product=num1*num2;
		double quotient=(double)num1/(double)num2;
		
		System.out.println("Sum of "+num1+" and "+num2+" : "+sum);
		System.out.println("Difference of "+num1+" and "+num2+" : "+diff);
		System.out.println("Product of "+num1+" and "+num2+" : "+product);
		System.out.println("Quotient when "+num1+" is divided by "+num2+" : "+quotient);
		

	}

}
